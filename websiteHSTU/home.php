    <div class="pera">
        <span>H</span>ajee Mohammad Danesh Science & Technology University is the first Science and Technology University in the northern region of Bangladesh. It stands away from the urban Dinajpur and bustle at a beautiful and scenic location some 13km north of Dinajpur town by the side of the intercity highway that links Dinajpur to Dhaka, the capital of Bangladesh. Hajee Mohammad Danesh Science and Technology University was established as an Agricultural Extension Training Institute (AETI) to award a three-year diploma in agriculture. The AETI was later upgraded to Hajee Mohammad Danesh Agricultural College in 1988 having an affiliation from the Bangladesh Agricultural University, Mymensingh. Then the college was upgraded to the status of a university renaming it as Hajee Mohammad Danesh Science and Technology University. First batch of the students were admitted at that time (1999-2000 session). The Act of the University was passed on 8 July 2001 in the Jatio Shongsad (National Assembly) was followed by a gazette notification on 8 April 2002. 
